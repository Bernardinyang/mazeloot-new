import * as LucideIcons from 'lucide-vue-next'
import { h, defineComponent } from 'vue'

function createAnimatedIcon(IconComponent) {
  if (!IconComponent || typeof IconComponent !== 'object') {
    return IconComponent
  }

  return defineComponent({
    name: `Animated${IconComponent.name || 'Icon'}`,
    props: IconComponent.props || {},
    setup(props, { attrs, slots }) {
      return () => h(IconComponent, {
        ...props,
        ...attrs,
        class: ['lucide-animated', attrs.class].filter(Boolean).join(' '),
      }, slots)
    },
  })
}

// Create animated versions of all icons
const wrapIcon = (icon) => {
  if (icon && typeof icon === 'object') {
    return createAnimatedIcon(icon)
  }
  return icon
}

