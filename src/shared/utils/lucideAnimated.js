// Export all remaining icons from lucide-vue-next (fallback for any not explicitly listed)
const allIcons = new Proxy({}, {
  get(target, prop) {
    if (prop in LucideIcons) {
      return wrapIcon(LucideIcons[prop])
    }
    return undefined
  },
  has(target, prop) {
    return prop in LucideIcons
  },
  ownKeys() {
    return Object.keys(LucideIcons)
  },
})

// Re-export everything else dynamically via default export
export default allIcons
